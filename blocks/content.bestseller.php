    for (i = 1; i <= 10; i++)
    {
        if (i <= 5)
        {
            $j("#bestseller-item-" + i).css("display", "none");
        }
        else
        {
            $j("#bestseller-item-" + i).css("display", "block");
        }
    }
    $j("#bestseller15").removeClass("active");
    $j("#bestseller610").addClass("active");
});
</script>